    @Override
    public void onLocationChanged(Location location) {

        double lat = location.getLatitude();
        double lng = location.getLongitude();
        double spd = location.getSpeed();
        float acc = location.getAccuracy();
        double alt = location.getAltitude();

        latitude.setText("Latitude : " + String.valueOf(lat));
        longitude.setText("Longitude : " + String.valueOf(lng));
        speed.setText("Speed : " + String.valueOf(spd));
        accuracy.setText("Accuracy : " + String.valueOf(acc));
        altitude.setText("Altitude : " + String.valueOf(alt));
        Geocoder geocoder = new Geocoder(getApplicationContext(), Locale.getDefault());
        try {
            List<Address> addr = geocoder.getFromLocation(lat, lng, 1);
            if (addr != null && addr.size() > 0) {
                address.setText("Address :\n" + addr.get(0).getSubLocality());
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

    }

